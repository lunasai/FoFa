const FALLBACK_BY_CATEGORY = {
  'sans-serif':  'sans-serif',
  'serif':       'serif',
  'monospace':   'monospace',
  'display':     'sans-serif',
  'handwriting': 'cursive',
}

export function genericFallback(category) {
  return FALLBACK_BY_CATEGORY[category] || 'sans-serif'
}

// Returns a CSS font-family stack string, e.g.  "Inter", sans-serif
export function buildStack(family, category) {
  return `"${family}", ${genericFallback(category)}`
}

// Builds a Google Fonts css2 stylesheet URL for a family + its weights.
export function buildGoogleFontUrl(family, weights) {
  const fam = String(family).trim().replace(/\s+/g, '+')
  const list = (weights && weights.length ? weights : [400]).slice().sort((a, b) => a - b)
  return `https://fonts.googleapis.com/css2?family=${fam}:wght@${list.join(';')}&display=swap`
}

// Parses a pasted Google Fonts URL (css2 embed URL or a specimen page URL)
// into { family, weights, category, url }. Returns null if it can't be parsed.
export function parseGoogleFontsUrl(raw) {
  const input = String(raw || '').trim()
  if (!input) return null
  const isGoogle = /fonts\.googleapis\.com|fonts\.google\.com/i.test(input)
  if (!isGoogle) return null

  // Case 1: embed/css URL with ?family=...
  const familyMatch = input.match(/[?&]family=([^&]+)/)
  if (familyMatch) {
    const decoded = decodeURIComponent(familyMatch[1])
    const [namePart, spec] = decoded.split(':')
    const family = namePart.replace(/\+/g, ' ').trim()
    let weights = []
    if (spec) {
      const at = spec.split('@')[1]
      if (at) {
        weights = at
          .split(';')
          .map(tok => {
            const parts = tok.split(',')
            const n = Number(parts[parts.length - 1])
            return Number.isFinite(n) ? n : null
          })
          .filter(Boolean)
        weights = [...new Set(weights)].sort((a, b) => a - b)
      }
    }
    const known = GOOGLE_FONTS.find(f => f.family.toLowerCase() === family.toLowerCase())
    const category = known?.category || 'sans-serif'
    return { family, weights, category, url: input }
  }

  // Case 2: specimen page URL  fonts.google.com/specimen/Family+Name
  const specimenMatch = input.match(/specimen\/([^/?#]+)/)
  if (specimenMatch) {
    const family = decodeURIComponent(specimenMatch[1]).replace(/\+/g, ' ').trim()
    const known = GOOGLE_FONTS.find(f => f.family.toLowerCase() === family.toLowerCase())
    const weights = known?.weights ?? [400, 700]
    const category = known?.category || 'sans-serif'
    return { family, weights, category, url: buildGoogleFontUrl(family, weights) }
  }

  return null
}
